public class ConvNegToPosNum {

	public static void main(String[] args) {
		int num = -40;
		if (num < 0)
		{
			//This is a negative number. Hence, converting to a positive number.
			num*=-1;
			System.out.println("The number -40 is converted to 40");
		}
	}
	
}
